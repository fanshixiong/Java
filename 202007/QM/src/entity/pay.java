package entity;

public class pay {
private int id;
private String leave;
private String belate;
private String absent;
private String basepay;
private String bonus;
private String depay;
private String rpay;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}

public String getLeave() {
	return leave;
}
public void setLeave(String leave) {
	this.leave = leave;
}
public String getBelate() {
	return belate;
}
public void setBelate(String belate) {
	this.belate = belate;
}
public String getAbsent() {
	return absent;
}
public void setAbsent(String absent) {
	this.absent = absent;
}
public String getBasepay() {
	return basepay;
}
public void setBasepay(String basepay) {
	this.basepay = basepay;
}
public String getBonus() {
	return bonus;
}
public void setBonus(String bonus) {
	this.bonus = bonus;
}
public String getDepay() {
	return depay;
}
public void setDepay(String depay) {
	this.depay = depay;
}
public String getRpay() {
	return rpay;
}
public void setRpay(String rpay) {
	this.rpay = rpay;
}



}
